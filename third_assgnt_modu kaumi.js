console.log(introduction); // print the introduction to the console, which will display "My name is Modu Kaumi, I am 32 years old and I am Nigerian."

// 2. Favorite quote or lyric, and convert to uppercase and lowercase.
let quote = "With God all things are possible."; // Create a variable for the quote and assign it a value
let upperQuote = quote.toUpperCase();  // Convert the quote to uppercase
let lowerQuote = quote.toLowerCase();  // Convert the quote to lowercase
console.log("Uppercase:", upperQuote);  // Print the uppercase quote to the console
console.log("Lowercase:", lowerQuote);  // Print the lowercase quote to the console

// 3. Reverse a word and log the reversed word.
let word = "technology"; // Create a variable for the word and assign it a value technology
let reversedWord = word.split("").reverse().join(""); // Split into array, reverse, then join back to string
console.log("Reversed word:", reversedWord); // Print the reversed word to the console, which will display "ygolonhcet"

// 4. Store prices of 3 items and calculate the total.
let mouse = 2000; // Create a variable for item1 and assign it a value of 2000
let system = 200000; // Create a variable for item2 and assign it a value of 200000
let key_board = 5000; // Create a variable for item3 and assign it a value of 5000
let total = mouse + system + key_board; // Calculate the total cost of the items
console.log("The total cost of the items is #" + total.toFixed(2)); // print total in to two decimal places , toFixed(2) keeps 2 decimal places

// 5. Find the average of 5 test scores.
